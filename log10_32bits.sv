module log10_32bits #(
    parameter WIDTH = 32,
    parameter NO_BITS = 5,
    parameter FRAC_BITS = 8,           // fixed-point precision
    parameter string LUT_FILE = "log2_lut.mem"
)(
    input  wire                     clk,
    input  wire                     rst,
    input  wire                     valid_in_i,
    input  wire [WIDTH-1:0]         avg_power_i,

    output reg  [15:0]              rssi_out_o,
    output reg                      valid_out_o
);

    // handle log(0)
    wire [WIDTH-1:0] x;
    assign x = (avg_power_i == 0) ? 1 : avg_power_i;

    // Leading-One Detector
    reg [NO_BITS-1:0] k;

    always @(*) begin
        casez (x)
            32'b1???????????????????????????????: k = 31;
            32'b01??????????????????????????????: k = 30;
            32'b001?????????????????????????????: k = 29;
            32'b0001????????????????????????????: k = 28;
            32'b00001???????????????????????????: k = 27;
            32'b000001??????????????????????????: k = 26;
            32'b0000001?????????????????????????: k = 25;
            32'b00000001????????????????????????: k = 24;
            32'b000000001???????????????????????: k = 23;
            32'b0000000001??????????????????????: k = 22;
            32'b00000000001?????????????????????: k = 21;
            32'b000000000001????????????????????: k = 20;
            32'b0000000000001???????????????????: k = 19;
            32'b00000000000001??????????????????: k = 18;
            32'b000000000000001?????????????????: k = 17;
            32'b0000000000000001????????????????: k = 16;
            32'b00000000000000001???????????????: k = 15;
            32'b000000000000000001??????????????: k = 14;
            32'b0000000000000000001?????????????: k = 13;
            32'b00000000000000000001????????????: k = 12;
            32'b000000000000000000001???????????: k = 11;
            32'b0000000000000000000001??????????: k = 10;
            32'b00000000000000000000001?????????: k = 9;
            32'b000000000000000000000001????????: k = 8;
            32'b0000000000000000000000001???????: k = 7;
            32'b00000000000000000000000001??????: k = 6;
            32'b000000000000000000000000001?????: k = 5;
            32'b0000000000000000000000000001????: k = 4;
            32'b00000000000000000000000000001???: k = 3;
            32'b000000000000000000000000000001??: k = 2;
            32'b0000000000000000000000000000001?: k = 1;
            32'b00000000000000000000000000000001: k = 0;
            default: k = 0;
        endcase
    end

    // Fraction Extraction
    reg [7:0] frac_index;

    always @(*) begin
    case (k)
        31: frac_index = x[30:23];
        30: frac_index = x[29:22];
        29: frac_index = x[28:21];
        28: frac_index = x[27:20];
        27: frac_index = x[26:19];
        26: frac_index = x[25:18];
        25: frac_index = x[24:17];
        24: frac_index = x[23:16];
        23: frac_index = x[22:15];
        22: frac_index = x[21:14];
        21: frac_index = x[20:13];
        20: frac_index = x[19:12];
        19: frac_index = x[18:11];
        18: frac_index = x[17:10];
        17: frac_index = x[16:9];
        16: frac_index = x[15:8];
        15: frac_index = x[14:7];
        14: frac_index = x[13:6];
        13: frac_index = x[12:5];
        12: frac_index = x[11:4];
        11: frac_index = x[10:3];
        10: frac_index = x[9:2];
        9 : frac_index = x[8:1];
        8 : frac_index = x[7:0];

        // padding cases
        7 : frac_index = {x[6:0], 1'b0};
        6 : frac_index = {x[5:0], 2'b00};
        5 : frac_index = {x[4:0], 3'b000};
        4 : frac_index = {x[3:0], 4'b0000};
        3 : frac_index = {x[2:0], 5'b00000};
        2 : frac_index = {x[1:0], 6'b000000};
        1 : frac_index = {x[0],   7'b0000000};
        0 : frac_index = 8'b0;

        default: frac_index = 8'b0;
    endcase
    end

    // LUT for log2(1+f)
    reg [15:0] log2_lut [0:255];

    initial begin
        $readmemh(LUT_FILE, log2_lut);
    end

    wire [15:0] frac_value;
    assign frac_value = log2_lut[frac_index];

    // log2 result
    wire [15:0] log2_fixed;
    assign log2_fixed = (k << FRAC_BITS) + frac_value;

    // scale to log10
    // Multiply by 3.01 ≈ 3 + 1/64
    wire [15:0] scaled;

    assign scaled = (log2_fixed << 1)   // 2x
                  + log2_fixed          // +1x = 3x
                  + (log2_fixed >> 6);  // +0.015x

    // Output Register
    always @(posedge clk or negedge rst) begin
        if (!rst) begin
            rssi_out_o  <= 0;
            valid_out_o <= 0;
        end else begin
            valid_out_o <= valid_in_i;
            if (valid_in_i)
                rssi_out_o <= scaled;
        end
    end

endmodule
